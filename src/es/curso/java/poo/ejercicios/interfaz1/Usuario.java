package es.curso.java.poo.ejercicios.interfaz1;

public class Usuario {

	private Coche coche;
	private Moto moto;
	private Movil movil;
	private Televisor tv;
	
	
	
	
	/**
	 * @return the coche
	 */
	public Coche getCoche() {
		return coche;
	}
	/**
	 * @param coche the coche to set
	 */
	public void setCoche(Coche coche) {
		this.coche = coche;
	}
	/**
	 * @return the moto
	 */
	public Moto getMoto() {
		return moto;
	}
	/**
	 * @param moto the moto to set
	 */
	public void setMoto(Moto moto) {
		this.moto = moto;
	}
	/**
	 * @return the movil
	 */
	public Movil getMovil() {
		return movil;
	}
	/**
	 * @param movil the movil to set
	 */
	public void setMovil(Movil movil) {
		this.movil = movil;
	}
	/**
	 * @return the tv
	 */
	public Televisor getTv() {
		return tv;
	}
	/**
	 * @param tv the tv to set
	 */
	public void setTv(Televisor tv) {
		this.tv = tv;
	}
	
	
	
	
}
